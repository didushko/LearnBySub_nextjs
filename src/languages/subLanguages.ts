export function getCountryCode(langCode: string) {
  let lang = subLanguages[langCode.toLowerCase()];
  if (!lang) {
    for (let key in subLanguages) {
      if (key.substring(0, key.indexOf("-")) == langCode.toLowerCase()) {
        return subLanguages[key].countryCode;
      }
    }
    return undefined;
  }
  return lang.countryCode;
}

export function getLangNameByCountryCode(countryCode: string, UILang = "en") {
   for (let [key, value] of Object.entries(subLanguages)) {
     if (value.countryCode === countryCode) {
       return value.title[UILang];
     }
   }
}
